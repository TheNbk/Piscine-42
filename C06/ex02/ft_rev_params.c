#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	j;
	int	size;

	i = 1;
	size = argc - 1;
	if (i < argc)
		i++;
	i = size;
	while (i < argc && argc > size && i > 0)
	{
		j = 0;
		while (argv[i][j])
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write (1, "\n", 1);
		i--;
	}
}
